;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;
;; Reset Handler
;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;
Reset:
    INIT_NES

	lda #State::TITLESCREEN
    sta GameState                   ; set GameState to TITLESCREEN

StateHandler:
	lda GameState

	cmp #State::TITLESCREEN
	bne :+
		jsr TitleScreen
		jmp StateHandler
	:

	cmp #State::LEVEL
	bne :+
		jsr Level
		jmp StateHandler
	:

	cmp #State::GAMEOVER
	bne :+
		jsr GameOverScreen
		jmp StateHandler
	:

	jmp StateHandler				; fallback to jump back to loop start

;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;
;; NMI Handler - Handles rendering tasks
;;      - Draw Sprite Data to OAM
;;      - Draw new Columns and new Attributes
;;      - Re-enable rendering for the PPU 
;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;
NMI:

SaveStackBuffer:
    SAVE_REGISTERS_BUFFER

DisableRendering:
    bit PPU_STATUS
    lda #%00010100              ; disable NMI, Set Background to second Pattern Table, 32-bit increment mode
    sta PPU_CTRL
    lda #$00                    ; disable rendering
    sta PPU_MASK

HandleSprites:
	jsr WriteSpriteData

SelectNMIHandler:
	lda GameState
	
	cmp #State::TITLESCREEN
	bne :+
		jsr TitleNMI
		jmp SetDrawComplete
	:

	cmp #State::LEVEL
	bne :+
		jsr LevelNMI
		jmp SetDrawComplete
	:

	cmp #State::GAMEOVER
	bne :+
		jsr GameOverNMI
		jmp SetDrawComplete
	:

SetDrawComplete:
    lda #1
    sta VBlankDone              ; set draw complete flag to true

RestoreStackBuffer:
    RESTORE_REGISTERS_BUFFER

    rti

